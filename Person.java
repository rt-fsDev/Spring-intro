package com.example.Spring_intro;

import com.fasterxml.jackson.annotation.JsonProperty;

public class Person {

    @JsonProperty("namn")
    private String name;
    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
